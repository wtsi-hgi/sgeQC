        dt_effcounts_pos_log <- reshape2::melt(effcounts_pos_log)
        colnames(dt_effcounts_pos_log) <- c("index", "samples", "log_counts")

        dt_effcounts_pos_log$samples <- factor(dt_effcounts_pos_log$samples, levels = sample_names)

